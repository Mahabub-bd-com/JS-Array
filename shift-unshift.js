// Array shift unshift 
const friend = ['balam', 'kalam', 'karim', 'rahim', 10, 20, 30];
console.log(friend);
friend.shift(); // shift means firt value remove 
friend.shift(); // shift means firt value remove
console.log(friend);
friend.unshift('I am Add'); // unshift means value add 
console.log(friend);